# Unpack a Collection
# If you have a collection of values in a list, tuple etc. Python allows you to extract the values into variables.
# This is called unpacking.
#
# Example
# Unpack a list:

fruits = ["apple", "banana", "cherry","plum","avocado"]
u, v, x, y, z = fruits
print(u)
print(v)
print(x)
print(y)
print(z)